package com.movieverse.events;

import java.io.Serializable;
import java.util.Date;
import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * POJO MainOrderEvent that is serialized in XML to be picked up by the payment service.
 * @author mghan
 */
@XmlRootElement
@XmlAccessorType(XmlAccessType.FIELD)
public class MainOrderEvent implements Serializable {
    
    private static final long serialVersionUID = -9117745111717872467L;

    private double amount;
    private Date dateCreated;
    private int customerID;
    private int orderID;
    private int statusID;
    
    public int getCustomerID() {
        return customerID;
    }

    public void setCustomerID(int customerID) {
        this.customerID = customerID;
    }

    public int getOrderID() {
        return orderID;
    }

    public void setOrderID(int orderID) {
        this.orderID = orderID;
    }    
    
    public Date getDateCreated() {
        return dateCreated;
    }

    public void setDateCreated(Date dateCreated) {
        this.dateCreated = dateCreated;
    }

    public int getStatusID() {
        return statusID;
    }

    public void setStatusID(int statusID) {
        this.statusID = statusID;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public String toString() {
        return "[MainOrderEvent] " + getCustomerID();
    }
}
